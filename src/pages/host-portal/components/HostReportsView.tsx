const statusBadge = (s: string) => {
  if (s === 'confirmed') return '<span class="badge badge-green">Confirmed</span>';
  if (s === 'completed') return '<span class="badge badge-stone">Completed</span>';
  if (s === 'pending')   return '<span class="badge badge-amber">Pending</span>';
  return '<span class="badge badge-stone">Cancelled</span>';
};

