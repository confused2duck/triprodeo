type ReportType = 'earnings' | 'bookings' | 'tax' | 'gst';

interface MonthlyGroup {
  key: string;
  label: string;
  year: number;
  month: number;
  bookings: HostBooking[];
  grossRevenue: number;
  platformFee: number;
  netEarnings: number;
  bookingCount: number;
  totalNights: number;
}

function groupByMonth(bookings: HostBooking[]): MonthlyGroup[] {
  const map = new Map<string, HostBooking[]>();
  bookings
    .filter((b) => b.status !== 'cancelled')
    .forEach((b) => {
      const d = new Date(b.bookedAt);
      const key = `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}`;
      map.set(key, [...(map.get(key) ?? []), b]);
    });
  return Array.from(map.entries())
    .sort(([a], [b]) => b.localeCompare(a))
    .map(([key, bks]) => {
      const [yr, mo] = key.split('-').map(Number);
      const label = new Date(yr, mo - 1, 1).toLocaleDateString('en-IN', { month: 'long', year: 'numeric' });
      return {
        key,
        label,
        year: yr,
        month: mo,
        bookings: bks,
        grossRevenue: bks.reduce((s, b) => s + b.totalAmount, 0),
        platformFee: bks.reduce((s, b) => s + b.platformFee, 0),
        netEarnings: bks.reduce((s, b) => s + b.hostEarnings, 0),
        bookingCount: bks.length,
        totalNights: bks.reduce((s, b) => s + b.nights, 0),
      };
    });
}

// ── CSV generator ──────────────────────────────────────────────────────────
function downloadCSV(filename: string, rows: string[][]): void {
  const csv = rows
    .map((r) =>
      r.map((cell) => (cell.includes(',') || cell.includes('"') || cell.includes('\n')
        ? `"${cell.replace(/"/g, '""')}"` : cell))
        .join(',')
    )
    .join('\n');
  const blob = new Blob(['\uFEFF' + csv], { type: 'text/csv;charset=utf-8;' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  a.click();
  URL.revokeObjectURL(url);
}

